package com.cht.iot.persistence.entity.api;

public class IColumn {
	String name;
	String type;	
	
	public IColumn() {		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}	
}
